let currentLang = localStorage.getItem("finals_lang") || "ja";

function t(key) {
  return (LANGS[currentLang] || LANGS.ja)[key] || LANGS.ja[key] || key;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem("finals_lang", lang);
  document.documentElement.lang = {ja:"ja",ko:"ko",en:"en"}[lang] || "ja";
  applyLang();
  document.querySelectorAll(".langBtn").forEach(b => b.classList.toggle("active", b.dataset.lang === lang));
  // テーブル再描画（state ラベル・時刻表記が変わるため）
  if(typeof renderTable === "function" && typeof lastRows !== "undefined") renderTable(lastRows);
  if(typeof updateLoginStatus === "function") updateLoginStatus();
}

function applyLang() {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  document.querySelectorAll("[data-i18n-ph]").forEach(el => {
    el.placeholder = t(el.dataset.i18nPh);
  });
  document.querySelectorAll("[data-i18n-title]").forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
  // select options
  document.querySelectorAll("[data-i18n-opt]").forEach(el => {
    const keys = el.dataset.i18nOpt.split(",");
    [...el.options].forEach((opt, i) => { if(keys[i]) opt.text = t(keys[i]); });
  });
}

document.addEventListener("DOMContentLoaded", () => {
  applyLang();
  document.querySelectorAll(".langBtn").forEach(b => {
    b.addEventListener("click", () => setLang(b.dataset.lang));
    b.classList.toggle("active", b.dataset.lang === currentLang);
  });
});
